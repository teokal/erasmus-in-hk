                        <img class="right-img" src="./images/hnv_1.jpg" alt="Hluboká nad Vltavou - White Castle" title="Hluboká nad Vltavou - White Castle" />
                        Hluboká nad Vltavou is situated on the Vltava river, near České Budějovice. The town is known for the picturesque Tudor-style
                        Hluboká Castle, that is open to the public. The Townshend International School has been based in
                        the town since 1992. Settled since the Bronze Age, the area was the site of medieval Froburg Castle,
                        built high above the Vltava river. Once a possession of the Přemyslid king Ottokar II of Bohemia,
                        it later passed to the Vítkovci dynasty. Again purchased by the Bohemian Crown in the 14th century,
                        it was a favourite residence of Emperor Charles IV, who often visited the castle when residing in
