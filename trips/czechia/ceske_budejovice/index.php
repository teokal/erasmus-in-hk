                    <p>
                        The city was founded in 1265 by King Ottokar II of Bohemia, who granted its municipal charter in 1265. The siting and planning
                        of the city was carried out by the king's knight Hirzo. The settlers were coming from the Bohemian
                        Forest and Upper Austria. The royal city was created as a platform of the king's power in South Bohemia
                        and to counterbalance the powerful noble House of Rosenberg, which became extinct in 1611.
                    </p>

                    <p>
                        The vast majority of the city's population today are Czechs (94.9% in 2001), with 1.15% Slovaks. In the past, the city had
                        a significant proportion of ethnic Germans, who had formed the majority since medieval times. The
                        city remained a German-speaking enclave until 1880, after which Czechs became the majority. Until
                        the end of World War II, the city contained a significant German minority (about 15.5% in 1930).
                        For example, the ratios between the Germans and the Czechs were in 1880: 11,829 Germans to 11,812
                        Czechs, in 1890: 11,642 to 16,585, in 1900: 15,400 to 23,400, in 1910: 16,900 to 27,300 and in 1921:
                        7,415 to 35,800. The entire German population was forcibly expelled in 1945 under the Beneš decrees.
                    </p>

                    <p>
                        <img class="left-img" src="./images/Pivovar_Budvar_-_budova_2.jpg" alt="Pivovar Budvar - Building 2" title="Pivovar Budvar - Building 2">
                        <strong>Budweiser Budvar Brewery Budějovice</strong> has long been well known for the beer brewed there since
                        the 13th century. For a time, the town was the imperial brewery for the Holy Roman Emperor, and Budweiser
                        Bier (i.e. beer from Budweis) became, along with Pilsner from Plzeň, one of the best-known lagers.
                        Brewing remains a major industry. In 1256 the Svitavy brewery was founded there, which was closed
                        in 2002.
                    </p>

