                <p>Brno is the seat of judicial authority of the Czech Republic – it is the seat of the Constitutional Court,
                    the Supreme Court, the Supreme Administrative Court, and the Supreme Public Prosecutor's Office. The
                    city is also a significant administrative centre. It is the seat of a number of state authorities, including
                    the Ombudsman, and the Office for the Protection of Competition. Brno is also an important centre of
                    higher education, with 33 faculties belonging to 13 institutes of higher learning and about 89,000 students.
                </p>

                <p>Brno Exhibition Centre ranks among the largest exhibition centres in Europe (23rd in the world). The complex
                    opened in 1928 and established the tradition of large exhibitions and trade fairs held in Brno. Brno
                    hosts motorbike and other races on the Masaryk Circuit, a tradition established in 1930, in which the
                    Road Racing World Championship Grand Prix is one of the most prestigious races. Another cultural tradition
                    is an international fireworks competition, Ignis Brunensis, that usually attracts tens of thousands of
                    daily visitors.</p>

                <p>The most visited sights of the city include the Špilberk castle and fortress and the Cathedral of Saints
                    Peter and Paul on Petrov hill, two medieval buildings that dominate the cityscape and are often depicted
                    as its traditional symbols. The other large preserved castle near the city is Veveří Castle by Brno Reservoir.
                    This castle is the site of a number of legends, as are many other places in Brno. Another architectural
                    monument of Brno is the functionalist Villa Tugendhat which has been included on the UNESCO list of World
                    Heritage Sites. One of the natural sights nearby is the Moravian Karst.</p>

