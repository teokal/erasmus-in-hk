                    <p>Cologne was founded and established in Ubii territory in the 1st century AD as the Roman Colonia Claudia
                        Ara Agrippinensium, the first word of which is the origin of its name. An alternative Latin name
                        of the settlement is Augusta Ubiorum, after the Ubii. "Cologne", the French version of the city's
                        name, has become standard in English as well. The city functioned as the capital of the Roman province
                        of Germania Inferior and as the headquarters of the Roman military in the region until occupied by
                        the Franks in 462. During the Middle Ages it flourished on one of the most important major trade
                        routes between east and west in Europe.</p>
                    <p>Cologne was one of the leading members of the Hanseatic League and one of the largest cities north of
                        the Alps in medieval and Renaissance times. Prior to World War II the city had undergone several
                        occupations by the French and also by the British (1918–1926). Cologne was one of the most heavily
                        bombed cities in Germany during World War II, with the Royal Air Force (RAF) dropping 34,711 long
                        tons (35,268 tonnes) of bombs on the city. The bombing reduced the population by 95%, mainly due
                        to evacuation, and destroyed almost the entire city. With the intention of restoring as many historic
                        buildings as possible, the successful postwar rebuilding has resulted in a very mixed and unique
                        cityscape.
                    </p>
